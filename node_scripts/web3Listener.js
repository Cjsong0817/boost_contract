const factoryContractAddress = '0xEF05CC5Bf045d1876A0bd9e43784c62143c32DcF';

async function checkTransactionStatus(txHash) {
    try {
        const signer = provider.getSigner();
        const receipt = await provider.getTransactionReceipt(txHash);
        var args = "";
        const myContract = new ethers.Contract(factoryContractAddress, boostProtocolABI, signer);
        receipt.logs.forEach(log => {
            try {
                const parsedLog = myContract.interface.parseLog(log);
                // console.log('Parsed Log:', parsedLog);
                args = parsedLog.args;
            } catch (e) {
                // console.log('Log could not be parsed by this contract:', log);
            }
        });
        console.log(JSON.stringify(args[3].toNumber()));
    } catch (error) {
        console.error('Error fetching transaction receipt:', error);
    }
}
setInterval(checkTransactionStatus(txHash), 1000);
